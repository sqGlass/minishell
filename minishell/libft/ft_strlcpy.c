#include "libft.h"

size_t	ft_strlcpy(char *d, const char *s, size_t n)
{
	size_t	i;

	i = 0;
	if (n != 0)
	{
		while (i < n - 1 && s[i] != '\0')
		{
			d[i] = s[i];
			i++;
		}
	}
	if (n != 0)
		d[i] = '\0';
	return (ft_strlen(s));
}
